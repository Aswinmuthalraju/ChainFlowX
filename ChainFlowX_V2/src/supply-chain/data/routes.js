function withCurrentPosition(route) {
  const fraction = FRACTION_SEED[route.id] ?? 0.25;
  const now = Date.now();
  const totalMs = route.normalTransitHours * 3600 * 1000;
  const departureMs = now - totalMs * fraction;
  const p = interpolateAlongGreatCircle(route.from.lat, route.from.lng, route.to.lat, route.to.lng, fraction);

  return {
    ...route,
    normalTransitDays: Math.max(1, Math.round(route.normalTransitHours / 24)),
    chokepoint: route.chokepoints?.[0] || null,
    chokepointId: route.chokepoints?.length ? CHOKEPOINT_TO_ID[route.chokepoints[0]] || null : null,
    graphEdges: route.graphEdges || [],
    dnaMatch: null,
    currentRisk: route.currentRisk ?? route.baseRisk,
    currentPosition: {
      ...p,
      fraction,
      lastUpdate: new Date(departureMs).toISOString(),
      estimatedArrival: new Date(departureMs + totalMs).toISOString(),
    },
  };
}

