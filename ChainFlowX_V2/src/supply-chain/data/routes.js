export const interpolateRoutePosition = (route, currentTime = Date.now()) => {
  const { from, to, normalTransitHours, currentPosition } = route;
  const departureTime = new Date(currentPosition?.lastUpdate || Date.now()).getTime();
  const elapsedMs = Math.max(0, currentTime - departureTime);
  const totalMs = Math.max(1, normalTransitHours * 3600 * 1000);

  const fraction = Math.min(1, elapsedMs / totalMs);
  if (fraction >= 1) {
    return {
      lat: to.lat,
      lng: to.lng,
      fraction: 1,
      estimatedArrival: new Date(departureTime + totalMs).toISOString(),
      status: 'arrived',
    };
  }

  const point = interpolateAlongGreatCircle(from.lat, from.lng, to.lat, to.lng, fraction);
  return {
    ...point,
    fraction,
    estimatedArrival: new Date(departureTime + totalMs).toISOString(),
  };
};
